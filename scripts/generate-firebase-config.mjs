import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");

const config = {
  apiKey: process.env.NEXT_PUBLIC_FIREBASE_API_KEY ?? "",
  authDomain: process.env.NEXT_PUBLIC_FIREBASE_AUTH_DOMAIN ?? "",
  projectId: process.env.NEXT_PUBLIC_FIREBASE_PROJECT_ID ?? "",
  messagingSenderId:
    process.env.NEXT_PUBLIC_FIREBASE_MESSAGING_SENDER_ID ?? "",
  appId: process.env.NEXT_PUBLIC_FIREBASE_APP_ID ?? "",
};

const jsonPath = path.join(root, "public", "firebase-config.json");
fs.writeFileSync(jsonPath, `${JSON.stringify(config, null, 2)}\n`, "utf8");
console.log(`Wrote ${jsonPath}`);

/** Sync import for service worker — must not use fetch on worker startup */
const swConfigPath = path.join(root, "public", "firebase-sw-config.js");
const swConfigBody = `/* Auto-generated by scripts/generate-firebase-config.mjs — do not edit */
self.__INDY_FIREBASE_CONFIG__ = ${JSON.stringify(config, null, 2)};
`;
fs.writeFileSync(swConfigPath, swConfigBody, "utf8");
console.log(`Wrote ${swConfigPath}`);
